NebulaOrb.MouseButton1Click:Connect(function()
	--last updated
	--2/26/25
	local player = game.Players.LocalPlayer
	local character = player.Character or player.CharacterAdded:Wait()
	local humanoidRootPart = character:WaitForChild("HumanoidRootPart")
	local part = Instance.new("Part")
	part.Shape = Enum.PartType.Ball
	part.Size = Vector3.new(1, 1, 1)
	part.Anchored = true
	part.CanCollide = false
	part.Color = Color3.fromRGB(0, 0, 0)  -- Black
	part.Material = Enum.Material.Neon
	part.Parent = game.Workspace
	local radius = 5  -- Orbit radius
	local speed = 3   -- Orbit speed (higher = faster)
	local amplitude = 2  -- Amplitude for up/down movement
	local frequency = 2  -- Frequency of up/down movement (higher = faster)
	local chatBubble = nil
	local textLabel = nil
	local function createChatBubble(text, position, duration)
		local chatBubble = Instance.new("BillboardGui")
		chatBubble.Adornee = part
		chatBubble.Size = UDim2.new(0, 200, 0, 50)
		chatBubble.StudsOffset = position
		chatBubble.AlwaysOnTop = true
		chatBubble.Parent = part
		local textLabel = Instance.new("TextLabel")
		textLabel.Size = UDim2.new(1, 0, 1, 0)
		textLabel.BackgroundTransparency = 1
		textLabel.TextColor3 = Color3.fromRGB(255, 255, 255)
		textLabel.TextStrokeTransparency = 0.5
		textLabel.TextSize = 20
		textLabel.Text = text
		textLabel.Parent = chatBubble
		delay(duration, function()
			if chatBubble then
				chatBubble:Destroy()
			end
		end)
	end
	local function changeChatBubbleText(newText)
		if textLabel then
			textLabel.Text = newText
		end
	end
	local trailParts = {}
	local trailLifetime = 2 
	local function createTrailPart(position)
		local trailPart = Instance.new("Part")
		trailPart.Size = Vector3.new(0.32, 0.32, 0.32)
		trailPart.Anchored = true
		trailPart.CanCollide = false
		trailPart.Color = Color3.fromRGB(0, 0, 0)
		trailPart.Material = Enum.Material.Neon
		trailPart.Position = position
		trailPart.Parent = game.Workspace
		local tweenService = game:GetService("TweenService")
		local goal = { Transparency = 1 }
		local tweenInfo = TweenInfo.new(trailLifetime, Enum.EasingStyle.Linear, Enum.EasingDirection.Out)
		local tween = tweenService:Create(trailPart, tweenInfo, goal)
		tween:Play()
		tween.Completed:Connect(function()
			trailPart:Destroy()
		end)
		table.insert(trailParts, trailPart)
	end
	local angle = 0
	local function orbitAndMove()
		game:GetService("RunService").Heartbeat:Connect(function()
			local x = humanoidRootPart.Position.X + radius * math.cos(angle)
			local z = humanoidRootPart.Position.Z + radius * math.sin(angle)
			local y = humanoidRootPart.Position.Y + amplitude * math.sin(frequency * angle)
			part.Position = Vector3.new(x, y, z)
			createTrailPart(part.Position)
			if angle % (2 * math.pi) < 0.1 then
				print("Orbiting")
			end
			angle = angle + speed * 0.01
		end)
	end
	orbitAndMove()
	game.Players.PlayerAdded:Connect(function(player)
		createChatBubble(player.Name .. " has joined the game!", Vector3.new(0, 5, 0))
	end)
	game.Players.PlayerRemoving:Connect(function(player)
		createChatBubble(player.Name .. " has left the game.", Vector3.new(0, 5, 0))
	end)
	createChatBubble("Hello, "..player.Name.."! Nebula Orb v1 by Anonymous", Vector3.new(0, 3, 0), 2)
	wait(2)
	createChatBubble("FE Admin commands coming soon!", Vector3.new(0, 3, 0), 4)
end)

