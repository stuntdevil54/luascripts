Findservertools.MouseButton1Click:Connect(function()
	local function findToolsInParent(parent)
		local tools = {}
		for _, object in pairs(parent:GetDescendants()) do
			if object:IsA("Tool") then
				table.insert(tools, object)
			end
		end
		return tools
	end
	local function searchForTools()
		local foundTools = {}
		local lightingTools = findToolsInParent(game.Lighting)
		for _, tool in pairs(lightingTools) do
			table.insert(foundTools, tool)
		end
		for _, player in pairs(game.Players:GetPlayers()) do
			local playerTools = findToolsInParent(player:WaitForChild("Backpack")) -- Inside Backpack for tools, StarterTools is used at start
			for _, tool in pairs(playerTools) do
				table.insert(foundTools, tool)
			end
		end
		local workspaceTools = findToolsInParent(game.Workspace)
		for _, tool in pairs(workspaceTools) do
			table.insert(foundTools, tool)
		end
		return foundTools
	end
	local player = game.Players.LocalPlayer
	local function giveToolsToPlayer(tools)
		for _, tool in pairs(tools) do
			local clonedTool = tool:Clone()
			clonedTool.Parent = player.Backpack
		end
	end
	local tools = searchForTools()
	giveToolsToPlayer(tools)
end)

